import type { VisionAnalysisContext } from '@/src/lib/analysis/openai/types';

export function createOpenAiPrompt(context: VisionAnalysisContext): string {
  const levelGuide =
    context.sightengineLevel === 'high'
      ? '외부 탐지 점수가 높습니다. 이미지 안에서 사람이 확인할 수 있는 시각적 의심 요소를 우선 찾으세요.'
      : context.sightengineLevel === 'medium'
        ? '외부 탐지 점수가 보통입니다. 뚜렷하게 보이는 시각적 단서만 신중하게 찾으세요.'
        : '외부 탐지 점수가 낮습니다. 억지로 의심 요소를 만들지 말고, 명확히 보이는 관찰 포인트가 없으면 비워두세요.';

  return [
    '당신은 이미지의 AI 여부를 최종 판정하지 않습니다.',
    '역할은 사용자가 직접 확인할 수 있는 시각적 관찰 포인트를 설명하는 것입니다.',
    '',
    `참고 정보: Sightengine score=${context.sightengineScore}, level=${context.sightengineLevel}.`,
    levelGuide,
    '',
    '분석 원칙:',
    '- 외부 탐지 점수를 다시 계산하거나 부정하지 마세요.',
    '- 실제 이미지에서 특정 위치로 확인 가능한 내용만 작성하세요.',
    '- 보이지 않는 근거를 추측하지 마세요.',
    '- 의심 요소는 최대 3개만 반환하세요.',
    '- 명확한 관찰 포인트가 없으면 suspicions는 빈 배열로 반환하세요.',
    '',
    '우선 확인할 시각 단서:',
    '- 손가락, 관절, 얼굴, 눈, 치아처럼 형태가 어색한 부분',
    '- 글자, 로고, 숫자처럼 세부 형태가 무너진 부분',
    '- 그림자, 빛 방향, 반사처럼 장면의 일관성이 맞지 않는 부분',
    '- 머리카락, 피부, 배경처럼 경계가 흐릿하거나 반복되는 부분',
    '- 손과 물체가 닿는 부분처럼 연결이 부자연스러운 부분',
    '- 반복 패턴, 비정상적인 질감, 형태 왜곡이 보이는 부분',
    '',
    '문장 작성 규칙:',
    '- title은 짧은 한국어 명사형으로 작성하세요.',
    '- title은 10자 이내를 권장합니다.',
    '',
    '- description은 결과 목록 카드용입니다.',
    '- 사용자가 무엇을 봐야 하는지 바로 이해할 수 있도록 1문장으로 요약하세요.',
    '- 관찰된 특징만 설명하세요.',
    '',
    '- detailDescription은 상세 분석용입니다.',
    '- 이미지에서 실제로 관찰되는 형태, 경계, 질감, 명암, 연결 상태를 1~2문장으로 설명하세요.',
    '- 사용자가 확대해서 직접 확인할 수 있는 내용을 중심으로 작성하세요.',
    '',
    '- description과 detailDescription은 같은 문장을 반복하지 마세요.',
    '- detailDescription은 description을 조금 더 구체적으로 설명하는 수준으로 작성하세요.',
    '',
    '- "실제 사진에서는", "정상적으로는", "보통은", "기대됩니다" 같은 비교 문장을 사용하지 마세요.',
    '- "AI 이미지에서 자주", "생성 이미지에서 흔히" 같은 추론 문장을 사용하지 마세요.',
    '- "자연스러워야 합니다", "분리되어야 합니다" 같은 기준 비교 문장을 사용하지 마세요.',
    '',
    '- 영어 기술 용어를 사용하지 마세요.',
    '- anatomy inconsistency, diffusion artifact 같은 표현을 사용하지 마세요.',
    '',
    '- 문장은 반드시 구체적인 대상부터 시작하세요.',
    '- 예: "머리카락 경계", "귀 주변 영역", "유리잔 반사", "손가락 형태"',
    '- 조사("는", "가")만으로 시작하지 마세요.',
    '',
    '- 단정 표현을 피하세요.',
    '- 주로 "처럼 보입니다", "관찰됩니다", "확인됩니다"를 사용하세요.',
    '',
    '좋은 예:',
    '- "귀 주변 머리카락의 가장자리가 선명하게 분리되지 않고 부드럽게 퍼져 보입니다."',
    '- "로고 일부의 획이 일정하지 않고 경계가 흐릿하게 이어져 보입니다."',
    '- "손가락 사이 간격이 일정하지 않고 형태가 다르게 표현되어 있습니다."',
    '',
    '나쁜 예:',
    '- "실제 사진에서는 머리카락이 더 자연스럽게 분리되는 것이 기대됩니다."',
    '- "이런 특징은 AI 이미지에서 자주 나타납니다."',
    '- "정상적인 사진이라면 이런 현상이 없습니다."',
    '',
    'summary 작성 규칙:',
    '- 결과 상단에 노출되는 전체 요약입니다.',
    '- Sightengine 점수와 모순되지 않도록 작성하세요.',
    '- 의심 요소가 발견되면 관찰 가능한 특징이 확인되었다고 설명하세요.',
    '- 의심 요소가 없다면 사람이 눈으로 확인할 수 있는 관찰 포인트는 제한적이라고 설명하세요.',
    '',
    'caution 작성 규칙:',
    '- 결과를 단정하지 마세요.',
    '- 사용자가 직접 이미지를 함께 확인하도록 유도하세요.',
    '- AI 여부를 확정적으로 표현하지 마세요.',
    '',
    'evidenceStrength:',
    '- AI 확률이 아닙니다.',
    '- 사용자가 눈으로 관찰하기 쉬운 정도입니다.',
    '- low | medium | high 중 하나만 사용하세요.',
    '',
    '좌표 규칙:',
    '- 모든 좌표는 원본 이미지 기준 퍼센트(0~100)입니다.',
    '- area는 의심되는 특징이 실제로 보이는 부분만 감싸는 작은 사각형으로 지정하세요.',
    '- area에 넓은 주변 맥락, 얼굴 전체, 물체 전체, 배경 전체를 포함하지 마세요.',
    '- marker는 반드시 area의 정중앙 좌표로 지정하세요.',
    '- 좌표가 불확실하면 해당 의심 요소를 반환하지 마세요.',
    '',
    '반드시 JSON만 반환하세요.',
    '마크다운, 코드블록, 추가 설명은 금지입니다.',
    '',
    'JSON 스키마:',
    '{',
    '  "hasVisibleEvidence": boolean,',
    '  "summary": string,',
    '  "caution": string,',
    '  "suspicions": [',
    '    {',
    '      "id": string,',
    '      "title": string,',
    '      "description": string,',
    '      "detailDescription": string,',
    '      "evidenceStrength": "high" | "medium" | "low",',
    '      "category": "anatomy" | "lighting" | "texture" | "background" | "artifact" | "text",',
    '      "area": { "x": number, "y": number, "width": number, "height": number },',
    '      "marker": { "index": number, "x": number, "y": number },',
    '      "priority": number',
    '    }',
    '  ]',
    '}',
  ].join('\n');
}
